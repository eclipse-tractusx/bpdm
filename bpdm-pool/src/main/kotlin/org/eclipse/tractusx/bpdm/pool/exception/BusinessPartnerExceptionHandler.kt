@RestControllerAdvice
class BusinessPartnerExceptionHandler : ResponseEntityExceptionHandler() {

    @ExceptionHandler(BusinessPartnerSearchException::class)
    fun handleBusinessPartnerSearchException(
        ex: BusinessPartnerSearchException
    ): ResponseEntity<Map<String, Any>> {

        val body = mapOf(
            "code" to HttpStatus.BAD_REQUEST.value(),
            "status" to "BAD_REQUEST",
            "message" to ex.message
        )

        return ResponseEntity.status(HttpStatus.BAD_REQUEST).body(body)
    }
}